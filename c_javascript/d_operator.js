let a = 10; // 10을 a에 할당

// 더하기 후 할당
a += 5; // a = a + 5  // 15

// 빼기 후 할당

a -= 2; // a = a - 2  // 13

// 곱하기 후 할당

a *= 2; // a = a * a  // 26

// 나누기 후 할당 

a /= 2; // a = a / 2  // 13

console.log(a);

